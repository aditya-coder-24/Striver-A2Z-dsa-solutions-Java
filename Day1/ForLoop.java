package com.mycompany.Day1;

public class ForLoop {

    public static void main(String[] args) {
        String x = "ArraysAreShihhh";
        for (int i = 0; i <= 10; i++) {
            System.out.println(x);
        }

        // While
        int j = 0;
        while (j < 5) {
            String y = "Strings are boring";
            System.out.println(y);
            j++;
        }
    }
}
